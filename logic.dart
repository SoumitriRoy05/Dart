

void main()
{
  //logic -if, if/else, if else if

  //if
  var num=5;
  if (num==5)
  {
    print("Num is 5!");
  }
  
  //if/else
  if(num%2==0)
  {
    print("Even number");
  }
  else
  {
    print("Odd number");
  }

  //if-else-if
  if(num==5)
  {
    print("Num is 5!");
  }
  else if(num==3)
  {
    print("The number is 3");
  }
  else{
    print("The number is not 5!");
  }
}